# -*- coding: utf-8 -*-
"""
normalizer.py —— 领域归一化模块

职责：
  将不同数据源中表述不一致但语义相同的领域关键词，统一映射到标准化的大类名称。
  例如能力侧的"人工智能（软件）"和机会侧可能出现的"AI+大模型"都归为"人工智能"。

使用方式：
  from normalizer import normalize_domain
  big_class = normalize_domain("人工智能（软件）")  # → "人工智能"

注意：本映射表基于当前数据中的实际领域值手工梳理。
如果后续 Excel 数据源新增了领域类别，需在此文件补充映射条目。
"""

# ============================================================================
# 领域 → 大类 归一化映射表
# ============================================================================
# key:   原始数据中出现的领域值（来自两张表的领域字段）
# value: 归一化后的大类名称
# ============================================================================
DOMAIN_MAP = {
    # ---- 人工智能大类 ----
    '人工智能（软件）': '人工智能',
    '人工智能（硬件）': '人工智能',

    # ---- 低空 / 航空航天 ----
    '低空经济（航空航天）': '低空经济',

    # ---- 信息通信大类 ----
    '新一代信息通信': '信息通信',

    # ---- 新材料 ----
    '新材料': '新材料',

    # ---- 智能制造大类 ----
    '智能制造装备': '智能制造',
    '机器人':      '智能制造',

    # ---- 智能电网 ----
    '智能电网': '能源电力',

    # ---- 新能源汽车大类 ----
    '智能网联新能源汽车': '新能源汽车',

    # ---- 生物医药 ----
    '生物医药': '生物医药',

    # ---- 集成电路大类 ----
    '集成电路': '集成电路',

    # ---- 场景机会侧的领域分类（与能力侧不一致，单独映射） ----
    '产业转型升级':          '综合',
    '六大新兴产业集群':      '综合',
    '其他':                 '综合',
    '数字民生服务':          '数字民生',
    '社会综合治理':          '城市治理',
}


def normalize_domain(raw_domain):
    """
    将原始领域字符串归一化到标准大类。

    参数:
        raw_domain (str): 原始领域值，如 "人工智能（软件）"

    返回:
        str: 归一化后的大类名称。如果映射表中没有对应条目，返回原始值。
             例如 "人工智能（软件）" → "人工智能"
                  "未知领域"       → "未知领域"（未命中映射表则原样返回）

    示例:
        >>> normalize_domain("人工智能（软件）")
        '人工智能'
        >>> normalize_domain("其他")
        '综合'
    """
    if not raw_domain:
        return '未知'
    return DOMAIN_MAP.get(raw_domain.strip(), raw_domain)
