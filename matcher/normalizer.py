# -*- coding: utf-8 -*-
"""
normalizer.py —— 领域归一化模块

============================================
模块职责
============================================
将能力侧和机会侧两个不同数据源中表述不一致的领域关键词，
统一映射到标准化的大类名称，确保跨数据源的领域匹配能够正确进行。

============================================
为什么需要归一化
============================================
场景能力数据（Excel Sheet "创新产品发布审核列表"）和
场景机会数据（Excel Sheet "应用场景发布审核"）的来源不同，
同一个产业领域在两边的表述可能完全不一样。例如：

  能力侧可能写: 人工智能（软件）
  机会侧可能写: 六大新兴产业集群  (一个覆盖了包括AI在内的多个领域的大分类)

如果不做归一化，两边的 domain 字段直接进行字符串比较将无法匹配，
导致应该匹配上的能力和机会被遗漏。

通过归一化映射表 DOMAIN_MAP，将两边都映射到同一套大类名称，
后续的 domain_match 维度才能正确计算匹配分。

============================================
映射规则概述（共 13 条映射规则）
============================================
本映射表基于当前数据中的实际领域值手工梳理，覆盖两大类数据源：

能力侧领域值（7 条映射）:
  1. 人工智能（软件）      → 人工智能
  2. 人工智能（硬件）      → 人工智能
  3. 低空经济（航空航天）  → 低空经济
  4. 新一代信息通信        → 信息通信
  5. 智能制造装备          → 智能制造
  6. 机器人                → 智能制造
  7. 智能网联新能源汽车    → 新能源汽车

机会侧领域值（6 条映射）:
  8. 智能电网              → 能源电力
  9. 产业转型升级          → 综合
  10. 六大新兴产业集群     → 综合
  11. 其他                 → 综合
  12. 数字民生服务         → 数字民生
  13. 社会综合治理         → 城市治理

自映射（4 条）:
  - 新材料    → 新材料    （两边叫法一致，写出来是为了文档完备性）
  - 生物医药  → 生物医药
  - 集成电路  → 集成电路
  - 智能制造  → 智能制造  （在 DOMAIN_MAP 中由"智能制造装备"和"机器人"映射而来）

============================================
维护说明
============================================
如果后续 Excel 数据源新增了领域类别，需在此文件补充映射条目。
未命中映射表的领域值将原样返回，不会报错，但可能导致匹配不正确。

建议操作流程:
  1. 查看启动日志中的领域值列表
  2. 判定每个新领域值应归入哪个大类
  3. 在 DOMAIN_MAP 中添加新条目
  4. 重启服务即可生效
"""

# ============================================================================
# 领域 → 大类 归一化映射表
# ============================================================================
# 格式: key = 原始数据中出现的领域值（来自两张表的领域字段）
#       value = 归一化后的大类名称
#
# 设计原则:
#  - 颗粒度统一: 所有大类都是同一抽象层级（如"人工智能"而非"AI软件平台"）
#  - 无歧义: 每个原始领域值只映射到一个大类
#  - 保守性: 不确定归属的领域值暂不映射，等待人工确认后再补充
# ============================================================================
DOMAIN_MAP = {
    # ========================================================================
    # 人工智能大类
    # ========================================================================
    # 包含人工智能的软件和硬件两个子方向。
    # 能力表中区分了软硬件，但机会表中不做区分，
    # 因此统一归为"人工智能"大类。
    '人工智能（软件）': '人工智能',
    '人工智能（硬件）': '人工智能',

    # ========================================================================
    # 低空 / 航空航天
    # ========================================================================
    # 低空经济涵盖无人机、通用航空、城市空中交通等子领域。
    # 能力表中用"（航空航天）"标注，映射到更简洁的"低空经济"。
    '低空经济（航空航天）': '低空经济',

    # ========================================================================
    # 信息通信大类
    # ========================================================================
    # 能力表中标注为"新一代信息通信"，包含 5G/6G、光通信、卫星通信等。
    # 机会表中直接使用"信息通信"，因此去掉"新一代"前缀以保持一致。
    '新一代信息通信': '信息通信',

    # ========================================================================
    # 新材料
    # ========================================================================
    # 新材料在能力和机会两边的叫法一致，但为了代码统一性仍保留显式映射。
    # 显式映射比 self-mapping 默认行为更能表明"这是经过确认的"。
    '新材料': '新材料',

    # ========================================================================
    # 智能制造大类
    # ========================================================================
    # 智能制造涵盖装备制造、工业机器人等子领域。
    # 能力表中将"智能制造装备"和"机器人"分开列出，但都属于智能制造大范畴。
    '智能制造装备': '智能制造',
    '机器人':      '智能制造',

    # ========================================================================
    # 能源电力大类
    # ========================================================================
    # "智能电网"出现在机会表中，属于能源电力领域的数字化升级方向。
    '智能电网': '能源电力',

    # ========================================================================
    # 新能源汽车大类
    # ========================================================================
    # 能力表中标注为"智能网联新能源汽车"，包含电动化 + 智能化的完整概念。
    # 简化为"新能源汽车"以匹配机会侧的表述。
    '智能网联新能源汽车': '新能源汽车',

    # ========================================================================
    # 生物医药
    # ========================================================================
    '生物医药': '生物医药',

    # ========================================================================
    # 集成电路大类
    # ========================================================================
    '集成电路': '集成电路',

    # ========================================================================
    # 场景机会侧的领域分类（与能力侧不一致，单独映射）
    # ========================================================================
    # 以下条目来自机会表的领域字段，与能力表的分类体系不同。
    # 机会表使用的是应用场景分类，偏业务视角，
    # 能力表使用的是产业领域分类，偏技术视角。
    # 两者本质上都是"领域"概念，但粒度不同，需要统一映射。

    # 综合类：产业转型升级、六大新兴产业集群、其他
    # 这些是覆盖多领域的宽泛分类，统一归为"综合"
    '产业转型升级':          '综合',
    '六大新兴产业集群':      '综合',
    '其他':                 '综合',

    # 数字民生：政府服务的数字化（政务服务、教育、医疗、文旅等）
    '数字民生服务':          '数字民生',

    # 城市治理：城市管理和社会治理的数字化（智慧城市、公共安全、交通等）
    '社会综合治理':          '城市治理',
}


def normalize_domain(raw_domain):
    """
    将原始领域字符串归一化到标准大类。

    本函数是领域匹配的前置步骤——在比较两个实体的领域是否相同时，
    先将两边的领域值分别调用 normalize_domain，再比较归一化后的结果。

    映射逻辑:
      1. 如果 raw_domain 为空字符串或 None → 返回 '未知'
      2. 如果 raw_domain 在 DOMAIN_MAP 中有匹配的 key → 返回映射后的值
      3. 如果 raw_domain 不在映射表中 → 返回原始值（原样透传）

    透传策略的意义:
      不在映射表中的领域值不会导致错误，而是以原始形式参与匹配。
      这使系统具备"渐进维护"特性：新增领域值初期可以正常工作，
      后续只需补充分类到映射表即可提升匹配精度。

    参数:
        raw_domain (str): 原始领域值，来源于 Excel 中的领域列。
                          例如 "人工智能（软件）"、"六大新兴产业集群"

    返回:
        str: 归一化后的大类名称。
             如果映射表中没有对应条目，返回原始值。
             如果输入为空，返回 '未知'。

    示例:
        >>> normalize_domain("人工智能（软件）")
        '人工智能'
        >>> normalize_domain("其他")
        '综合'
        >>> normalize_domain("量子计算")        # 未在映射表中
        '量子计算'                              # 原样返回
        >>> normalize_domain("")                # 空值
        '未知'
    """
    # 空值保护：传入空字符串或 None 时返回 '未知'
    # 避免查询 DOMAIN_MAP 时产生不可预期的行为
    if not raw_domain:
        return '未知'

    # 使用 dict.get(key, default) 实现"命中则映射，未命中则透传"
    # .strip() 去除可能存在的空格——Excel 数据中偶尔有尾部空白
    return DOMAIN_MAP.get(raw_domain.strip(), raw_domain)
